;PUERTO A CONFIGURADO COMO ENTRADA DIGITAL Y EL PUERTO B COMO SALIDA. EL DATO
;QUE ENTRE POR EL PUERTO A, DEBERÁ MOSTRARSE EN EL PUERTO B MULTIPLICADO POR
;CUALQUIER OTRA CANTIDAD.
;EL PROGRAMA SERÁ CICLICO O SE EJECUTARÁ DE MANERA INDEFINIDA.
