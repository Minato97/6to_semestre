;Enunciado del problema
;PRÁCTICA 3. MULTIPLICADOR POR n
;REALICE UN PROGRAMA PARA EL PIC16F887 CON EL PUERTO A CONFIGURADO COMO ENTRADA
;DIGITAL Y EL PUERTO B COMO SALIDA. EL DATO QUE ENTRE POR EL PUERTO A DEBERÁ 
;MOSTRARSE POR EL PUERTO B MULTIPLICADO POR n. SI EL DATA CAMBIA EN EL A, TAMBIÉN LO HARÁ EN EL B. EL PROGRAMA DEBERÁ CICLARSE. EL NÚMERO POR EL QUE SE MULTIPLICARÁ, DEBERÁ DEFINIRSE EN EL PROGRAMA.
