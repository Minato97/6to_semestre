			BSF	PORTB,0				;ENCENDER BIT PORQUE LA CONTRASE헤 ES CORRECTA
			BSF	PORTB,1
			BSF	PORTB,2
			BSF	PORTB,3
			BSF	PORTB,4
			BSF	PORTB,5
			BSF	PORTB,6
			BSF	PORTB,7
			GOTO	LEER

MAL			BCF	PORTB,0
